// Bundles bgutils-js into a single browser-side IIFE that exposes BG on
// window. We embed the resulting code as a JavaScript string so it can be
// injected into the WebView via WebView.injectedJavaScript at app start.
//
// Run: `node scripts/bundle-bgutils.cjs`
// Output: src/generated/bgutils-bundle.js (committed; CI doesn't re-run this)
//
// Re-run when bumping bgutils-js to pick up upstream BotGuard changes.

const esbuild = require('esbuild');
const fs = require('fs');
const path = require('path');

const outDir = path.join(__dirname, '..', 'src', 'generated');
fs.mkdirSync(outDir, { recursive: true });

const intermediatePath = path.join(outDir, 'bgutils.iife.js');

esbuild.buildSync({
  entryPoints: [require.resolve('bgutils-js')],
  bundle: true,
  format: 'iife',
  globalName: '__BGUTILS__',
  platform: 'browser',
  target: ['es2020'],
  minify: true,
  outfile: intermediatePath,
  banner: { js: '/* bgutils-js IIFE bundle for PlayFool WebView injection */' },
});

const code = fs.readFileSync(intermediatePath, 'utf-8');
const exposeBG = '\n;window.BG = (window.__BGUTILS__ && window.__BGUTILS__.BG) || window.__BGUTILS__;';
const finalJs = code + exposeBG;

const outPath = path.join(outDir, 'bgutilsBundle.js');
fs.writeFileSync(outPath, `// Auto-generated by scripts/bundle-bgutils.cjs. Do not edit by hand.\nexport const BGUTILS_CODE = ${JSON.stringify(finalJs)};\n`);
fs.unlinkSync(intermediatePath);

console.log(`Wrote ${outPath} (${(finalJs.length / 1024).toFixed(1)} KB of injectable JS)`);
